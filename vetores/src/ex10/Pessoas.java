package ex10;

public class Pessoas {
	double altura;
	char genero;
	
	public Pessoas(double altura, char genero) {
		super();
		this.altura = altura;
		this.genero = genero;
	}
	public double getAltura() {
		return altura;
	}
	public void setAltura(double altura) {
		this.altura = altura;
	}
	public char getGenero() {
		return genero;
	}
	public void setGenero(char genero) {
		this.genero = genero;
	}
	
	
}
